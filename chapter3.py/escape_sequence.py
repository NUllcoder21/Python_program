# Single and Double Quotes
print('It\'s a beautiful day!')  # Output: It's a beautiful day!
print("She said, \"Hello!\"")    # Output: She said, "Hello!"

# Newline and Tab
print("Hello\nWorld")            # Output: Hello (new line) World
print("Name:\tVishal")           # Output: Name:   Vishal (with a tab space)

# Backslash
print("This is a backslash: \\") # Output: This is a backslash: \

# Unicode and Hexadecimal
print("\u2764")                  # Output: ‚ù§ (Unicode character for heart)
print("\x41")                    # Output: A (Hexadecimal for 'A')

# Carriage Return
print("Hello\rWorld")            # Output: World (replaces Hello due to \r)

# Backspace
print("Hello\b!")                # Output: Hell! (removes 'o')

print(r"This is a raw string: \n\t\\")  # Output: This is a raw string: \n\t\\
