# A set in Python is an unordered collection of unique elements. It is:

# Unordered: No specific order for elements.
# Mutable: You can add or remove items.
# No duplicates: Automatically removes duplicate entries

s={1,2,3}
print(s)
print(type(s))

#Empty set

e=set()
print(e)
e.add(1)
print(e)
print(type(e))